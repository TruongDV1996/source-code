void CMainFrame::SetColumns(int nCols, CMFCToolBar & tbToolBox)
{
	int nColumns = nCols;
	int nCount = tbToolBox.GetCount();
	for (int i = 0; i < nCount; i++)
	{
		UINT nStyle = tbToolBox.GetButtonStyle(i);
		BOOL bWrap = (((i + 1) % nColumns)==0);
		if (bWrap)
			nStyle |= TBBS_WRAPPED;
		else
			nStyle &= ~TBBS_WRAPPED;
		tbToolBox.SetButtonStyle(i, nStyle);
	}
	RecalcLayout();
	Invalidate();
}

